export type District = { name: string };

export type City = {
  name: string;
  districts?: District[];
  lat: number;
  lng: number;
};

export type CityCoords = { lat: number; lng: number };

export function getCityCoords(name: string): CityCoords | null {
  for (const oblast of BELARUS_CITIES) {
    for (const city of oblast.cities) {
      if (city.name === name) return { lat: city.lat, lng: city.lng };
    }
  }
  return null;
}

